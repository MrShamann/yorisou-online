// O(1) lookup for the server route. Returns undefined for any unknown slug so
// the route can call notFound().
const REPORT_BY_FAMILY: ReadonlyMap<string, FamilyReport> = new Map(
  APP2_FAMILY_REPORTS.map((report) => [report.family, report]),
);

export function getFamilyReport(slug: string): FamilyReport | undefined {
  return REPORT_BY_FAMILY.get(slug);
}
