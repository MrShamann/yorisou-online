// APP-2 — Nine-family deeper-report content model (public-safe, device-local).
//
// ONE shared report architecture for the nine retained result families. Each
// family gets its own APPROVED, calm-Japanese content, but every report is
// rendered by the same server route + client view (app/reports/family/[family]).
//
// Honesty boundaries baked into the content (never softened at render time):
//   - NO fabricated science, NO percentages/statistics, NO clinical / medical /
//     psychological-diagnosis language, NO locked chapters, NO payment wall.
//   - The `methodology` field is grounded truthfully in how the result is
//     actually derived ("回答パターンから今の傾向を読み取っています"; imairo is
//     the 120Q family, so it is grounded in the 24-result mapping).
//   - The `limits` field always carries the not-a-diagnosis boundary.
//
// The family slugs are intentionally identical to lib/sr1/supportPlan.ts's
// SupportPlanFamily so a saved report composes with the existing device-local
// guest journey / support-plan surfaces without any taxonomy change.

